package com.mylearnings.java.core.wrapperclasss;

public class ValueOfMethod {

		public static void main(String[] args) 
		{ 
			Integer int1 = Integer.valueOf("10"); 
			System.out.println(int1); 
			Double dou = Double.valueOf("10.0"); 
			System.out.println(dou); 
			Boolean boo = Boolean.valueOf("true"); 
			System.out.println(boo); 
			//Integer int2 = Integer.valueOf("ten"); 
			Integer int2 = Integer.valueOf("20"); 
			System.out.println(int2); 
		} 
	} 
